#include "main.h"
#include <stdio.h>
#include <stdlib.h>
#include <ctype.h>
#include <string.h>

/**
 * _strcpy - copies a string to a buffer
 * @dest: the buffer to copy to
 * @src: the string to copy
 *
 * Return: a pointer to the buffer
 */
char *_strcpy(char *dest, char *src)
{
    return strcpy(dest, src);
}
